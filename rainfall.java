public class rainfall {
	
	public static void main(String[] args) {
		
		double april=12.0;
		double may=14.0;
		double june=8.0;

		double ave=april+may+june;
		double average=ave/3;

		System.out.print("\tRainfall for April:  "+ april);
		System.out.print("\n\tRainfall for May: " + may);
		System.out.print("\n\tRainfall for June: " + june);	
		System.out.println("\n\tAverage rainfall:  " +average);	


	}

}